    public void listarProdutosVendidos(){
        try {
            ProdutoDAO produtosdao = new ProdutoDAO();
            
            DefaultTableModel model = (DefaultTableModel) tblVendidos.getModel();
            model.setNumRows(0);
            
            ArrayList<Produto> listaVendidos = produtosdao.listarProdutosVendidos();
            
            for(int i = 0; i < listaVendidos.size(); i++){
                model.addRow(new Object[]{
                    listaVendidos.get(i).getId(),
                    listaVendidos.get(i).getNome(),
                    listaVendidos.get(i).getValor(),
                    listaVendidos.get(i).getStatus()
                });
            }
        } catch (Exception e) {
        }
    
    }
    
    
    
    
    
